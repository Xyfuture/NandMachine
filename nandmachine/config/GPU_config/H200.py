# Model notes:
# - H200_SXM and H200_NVL are both modeled in this file.
# - Both SKUs share tensor-core micro-architecture constants in this model.
# - Main modeled SKU differences are clock frequency and device labeling.
# - Both SKUs currently use 18 NVLink links in the communication profile.

# H200 SXM scalar parameters
H200_SXM_MEMORY_CAPACITY_BYTES = 141 * 1024**3
H200_HBM_STACK_COUNT = 6
H200_HBM_STACK_BYTES = (47 * 1024**3) // 2
H200_HBF_STACK_BYTES = 256 * 1024**3
