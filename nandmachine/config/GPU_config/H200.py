# H200 communication parameters
H200_NVLINK_BW_PER_DIRECTION_BPS = 25e9
H200_NVLINK_BW_BIDIR_BPS = 50e9
H200_SXM_NVLINK_LINK_COUNT_PER_DEVICE = 18
H200_NVL_NVLINK_LINK_COUNT_PER_DEVICE = 18
