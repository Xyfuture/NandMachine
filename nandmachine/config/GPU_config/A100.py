from __future__ import annotations

from nandmachine.config.GPU_config.schema import (
    ComputeModule,
    Core,
    Device,
    IOModule,
    SystolicArray,
    VectorUnit,
)

# Model notes:
# - This file defines one A100 SKU profile used by this project: A100 80GB FP16.
# - Unlike H100/H200 files, there is no intra-family split (such as SXM vs PCIe) here.

# A100 scalar parameters
A100_CORE_COUNT = 108
A100_CLOCK_FREQ_HZ = 1.41e9
A100_L2_SIZE_BYTES = 40 * 1024**2
A100_L2_BW_BYTES_PER_CYCLE = 5120
A100_IO_BW_BYTES_PER_SEC = 2039e9
A100_FLOPS_PER_EXP = 35

# A100 shared vector/tensor core parameters
A100_VECTOR_FLOPS_PER_CYCLE_FP16 = 512
A100_VECTOR_FLOPS_PER_CYCLE_FP8 = A100_VECTOR_FLOPS_PER_CYCLE_FP16 * 2
A100_MAC_PER_CYCLE_FP16 = 1
A100_MAC_PER_CYCLE_FP8 = A100_MAC_PER_CYCLE_FP16 * 2
A100_SYSTOLIC_ARRAY_HEIGHT = 16
A100_SYSTOLIC_ARRAY_WIDTH = 16
A100_SYSTOLIC_ARRAY_INPUT_WORD_SIZE = 2
A100_SYSTOLIC_ARRAY_OUTPUT_WORD_SIZE = 2
A100_SYSTOLIC_ARRAY_COUNT = 4
A100_SRAM_SIZE_BYTES = 192 * 1024

# A100 communication parameters
A100_NVLINK_BW_PER_DIRECTION_BPS = 25e9
A100_NVLINK_BW_BIDIR_BPS = 50e9
A100_NVLINK_LINK_COUNT_PER_DEVICE = 12
A100_NVLINK_TOTAL_BW_BPS = 600e9
A100_PCIE_GEN5_BW_PER_DIRECTION_BPS = 64e9
A100_LINK_LATENCY_SEC = 8.92e-6
A100_FLIT_SIZE_BYTES = 16
A100_MAX_PAYLOAD_SIZE_BYTES = 256
A100_HEADER_SIZE_BYTES = 16

A100_VECTOR_UNIT_FP16 = VectorUnit(
    A100_VECTOR_FLOPS_PER_CYCLE_FP16,
    A100_VECTOR_FLOPS_PER_CYCLE_FP8,
    A100_FLOPS_PER_EXP,
)
A100_SYSTOLIC_ARRAY_FP16 = SystolicArray(
    A100_SYSTOLIC_ARRAY_HEIGHT,
    A100_SYSTOLIC_ARRAY_WIDTH,
    A100_MAC_PER_CYCLE_FP16,
    A100_MAC_PER_CYCLE_FP8,
    A100_SYSTOLIC_ARRAY_INPUT_WORD_SIZE,
    A100_SYSTOLIC_ARRAY_OUTPUT_WORD_SIZE,
)
A100_CORE_FP16 = Core(
    vector_unit=A100_VECTOR_UNIT_FP16,
    systolic_array=A100_SYSTOLIC_ARRAY_FP16,
    systolic_array_count=A100_SYSTOLIC_ARRAY_COUNT,
    SRAM_size=A100_SRAM_SIZE_BYTES,
)

A100_COMPUTE_MODULE_FP16 = ComputeModule(
    core=A100_CORE_FP16,
    core_count=A100_CORE_COUNT,
    clock_freq=A100_CLOCK_FREQ_HZ,
    l2_size=A100_L2_SIZE_BYTES,
    l2_bandwidth_per_cycle=A100_L2_BW_BYTES_PER_CYCLE,
)
A100_IO_MODULE = IOModule(bandwidth=A100_IO_BW_BYTES_PER_SEC)
A100_80GB_FP16 = Device(
    compute_module=A100_COMPUTE_MODULE_FP16,
    io_module=A100_IO_MODULE,
)

